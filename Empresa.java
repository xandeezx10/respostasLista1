public class Empresa {
	private String nome;
	private String cnpj;
	public Empresa(String n, String cnpj) {
		this.nome = n;
		this.cnpj = cnpj;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getNome() {
		return nome = nome;
	}
	public String getCnpj() {
		return cnpj = cnpj;
	}
	public void setNome(){
		this.nome = nome;
	}
	public void setCnpj() {
		this.cnpj = cnpj;
	}
}
